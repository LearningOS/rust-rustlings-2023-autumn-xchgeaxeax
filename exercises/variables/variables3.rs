fn main() {
    let x: i64 = 0xdeadbeef;
    let y: i32 = 114_514;
    let u1: u32 = 0xdeadbeef;
    let mut z = 5;
    println!("Number x is {} and y is {}", x, y);
    println!("Unsigned num u1 is {}", u1);
    println!("z is {}", z);
    z = 10;
    println!("z is {}", z);

}
